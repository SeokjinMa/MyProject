package mwp;

public class FoodMenu {
	private String menu;
	private int fprice;
	
	public FoodMenu() {}

	public String getMenu() {
		return menu;
	}

	public void setMenu(String menu) {
		this.menu = menu;
	}

	public int getFprice() {
		return fprice;
	}

	public void setFprice(int fprice) {
		this.fprice = fprice;
	}
}
